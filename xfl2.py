# Perform String Operations

def reverse_string(sample_string):
    return sample_string[::-1]

def reverse_array(sample_array):
    return sample_array[::-1]

def concatenate_string(str_one, str_two):
    return str_one + str_two

print(reverse_string("summon me"))
print(reverse_array([1, 2, 3, 4, 5]))
print(concatenate_string("summon", "me"))